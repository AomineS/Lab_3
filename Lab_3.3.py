# Дано ціле число N (> 0). Знайти квадрат даного числа, використовуючи для його обчислення
#наступну формулу: N2= 1 + 3 + 5 + ... + (2 • N - 1).
#Виконав: Супрун Богдан
N = int(input("Ввести число: "))
C = [2 * x - 1 for x in range(1, N + 1)]
print("Порядок: ", C)
B = sum([2 * x - 1 for x in range(1, N + 1)])
print("Сума", B)
