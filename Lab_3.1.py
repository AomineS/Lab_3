#Визначити, чи є задане натуральне число паліндромом, тобто таким, десяткова запис якого
#читається однаково зліва направо і справа наліво
#Виконав: Супрун Богдан
n1 = int(input("Ввести число: "))
n2 = 0
n3 = n1
while n1 > 0:
    digital = n1 % 10
    n1 //= 10
    n2 *= 10
    n2 += digital
if n3 == n2:
    print("Число є паліндромом!")
else:
    print("Число не є паліндромом!")